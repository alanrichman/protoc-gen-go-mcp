type TplParams struct {
	PackageName string
	SourcePath  string
	GoPackage   string
	Tools       map[string]runtime.Tool
	ToolsOpenAI map[string]runtime.Tool
	Services    map[string]map[string]Tool
}

type Tool struct {
	RequestType   string
	ResponseType  string
	MCPTool       runtime.Tool
	MCPToolOpenAI runtime.Tool
}

// LazyTool holds method descriptors instead of pre-generated schemas
// to reduce memory consumption during generation
