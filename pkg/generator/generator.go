// messageSchema delegates to the gen package.
func (g *FileGenerator) messageSchema(md protoreflect.MessageDescriptor) map[string]any {
	return gen.MessageSchema(md, gen.SchemaOptions{OpenAICompat: g.openAICompat})
}

// getType delegates to the gen package.
func (g *FileGenerator) getType(fd protoreflect.FieldDescriptor) map[string]any {
	return gen.FieldSchema(fd, gen.SchemaOptions{OpenAICompat: g.openAICompat})
}

func (g *FileGenerator) Generate(packageSuffix string) {
	file := g.f
	if len(g.f.Services) == 0 {
		return
	}
	g.logf("Starting generation for file: %s", file.Desc.Path())

	goImportPath := file.GoImportPath
	if packageSuffix != "" {
		if !token.IsIdentifier(packageSuffix) {
			g.gen.Error(fmt.Errorf("package_suffix %q is not a valid Go identifier", packageSuffix))
			return
		}
		file.GoPackageName += protogen.GoPackageName(packageSuffix)
		generatedFilenamePrefixToSlash := filepath.ToSlash(file.GeneratedFilenamePrefix)
		file.GeneratedFilenamePrefix = path.Join(
			path.Dir(generatedFilenamePrefixToSlash),
			string(file.GoPackageName),
			path.Base(generatedFilenamePrefixToSlash),
		)
		goImportPath = protogen.GoImportPath(path.Join(
			string(file.GoImportPath),
			string(file.GoPackageName),
		))
	}

	g.gf = g.gen.NewGeneratedFile(
		file.GeneratedFilenamePrefix+GeneratedFilenameExtension,
		goImportPath,
	)
	if packageSuffix != "" {
		g.gf.Import(file.GoImportPath)
	}

	// Parse small templates once
	toolTpl, err := template.New("tool").Funcs(template.FuncMap{
		"quoteBytes": func(b []byte) string {
			return fmt.Sprintf("%q", string(b))
		},
	}).Parse(toolTemplate)
	if err != nil {
		g.gen.Error(err)
		return
	}

	handlerTpl, err := template.New("handler").Funcs(template.FuncMap{}).Parse(handlerTemplate)
	if err != nil {
		g.gen.Error(err)
		return
	}

	// Write file header
	g.gf.P("// Code generated by protoc-gen-mcp-go. DO NOT EDIT.")
	g.gf.P("// source: ", g.f.Desc.Path())
	g.gf.P()
	g.gf.P("package ", g.f.GoPackageName)
	g.gf.P()
	g.gf.P("import (")
	g.gf.P("  \"context\"")
	g.gf.P("  \"encoding/json\"")
	g.gf.P("  \"google.golang.org/protobuf/encoding/protojson\"")
	g.gf.P("  \"connectrpc.com/connect\"")
	g.gf.P("  grpc \"google.golang.org/grpc\"")
	g.gf.P("  \"github.com/redpanda-data/protoc-gen-go-mcp/pkg/runtime\"")
	g.gf.P(")")
	g.gf.P()
	g.gf.P()
	g.gf.P("var (")

	// Stream tool definitions - generate and write one at a time to reduce memory
	services := map[string]map[string]Tool{}

	for _, svc := range g.f.Services {
		g.logf("  Processing service: %s", svc.Desc.FullName())
		s := map[string]Tool{}

		for _, meth := range svc.Methods {
			if meth.Desc.IsStreamingClient() || meth.Desc.IsStreamingServer() {
				continue
			}
			g.logf("    Processing method: %s", meth.Desc.FullName())

			comment := string(meth.Comments.Leading)

			// Generate ONE tool at a time, write it, then let it be GC'd
			toolStandard, toolOpenAI := gen.ToolForMethod(meth.Desc, comment)
			toolKey := svc.GoName + "_" + meth.GoName

			// Execute tool template for standard variant
			if err := toolTpl.Execute(g.gf, map[string]any{
				"Key":  toolKey,
				"Tool": toolStandard,
			}); err != nil {
				g.gen.Error(err)
				return
			}

			// Execute tool template for OpenAI variant
			if err := toolTpl.Execute(g.gf, map[string]any{
				"Key":  toolKey + "OpenAI",
				"Tool": toolOpenAI,
			}); err != nil {
				g.gen.Error(err)
				return
			}

			// Store lightweight metadata (no schemas) for handler generation
			s[meth.GoName] = Tool{
				RequestType:   g.gf.QualifiedGoIdent(meth.Input.GoIdent),
				ResponseType:  g.gf.QualifiedGoIdent(meth.Output.GoIdent),
				MCPTool:       runtime.Tool{Name: toolStandard.Name},
				MCPToolOpenAI: runtime.Tool{Name: toolOpenAI.Name},
			}

			// toolStandard and toolOpenAI are now out of scope and can be GC'd
		}
		services[string(svc.Desc.Name())] = s
	}

	g.gf.P(")")
	g.gf.P()

	// Generate handler functions using template (lightweight - no schemas)
	params := TplParams{
		PackageName: string(g.f.Desc.Package()),
		SourcePath:  g.f.Desc.Path(),
		GoPackage:   string(g.f.GoPackageName),
		Services:    services,
		Tools:       nil, // Not needed - already written
		ToolsOpenAI: nil, // Not needed - already written
	}

	if err := handlerTpl.Execute(g.gf, params); err != nil {
		g.gen.Error(err)
		return
	}

	g.logf("Finished generation for file: %s", file.Desc.Path())
}
